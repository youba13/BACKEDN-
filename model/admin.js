const mongoose = require("mongoose")

const adminSchema = new mongoose.Schema({
    nom: {
        type: String,
        required: true,
    },
    prenom: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        unique: true,
        required: true,
    },
    password: {
        type: String,
        required: true,
    },
    role: {
        type: String,
        default: "admin"
    }
});
const AdminModel =mongoose.model("admin", adminSchema)
module.exports = {AdminModel} 